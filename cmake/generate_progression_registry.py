#!/usr/bin/env python3
"""
generate_progression_registry.py

Scans progression script headers for @progbox-register blocks
and generates a C++ header with registry entries.
"""

import argparse
import re
import sys
from pathlib import Path
from dataclasses import dataclass
from typing import List, Optional


@dataclass
class ProgressionEntry:
    id: str
    name: str
    class_name: str
    header: str


def parse_register_block(content: str, filepath: Path) -> Optional[ProgressionEntry]:
    """Parse a single @progbox-register block from file content."""
    pattern = r'@progbox-register\s*\n((?:\s*///\s*.*\n)*?)\s*///\s*@end-progbox-register'
    
    match = re.search(pattern, content)
    if not match:
        return None
    
    block = match.group(1)
    
    id_match = re.search(r'id:\s*(\w+)', block)
    name_match = re.search(r'name:\s*"([^"]+)"', block)
    class_match = re.search(r'class:\s*(\w+)', block)
    
    errors = []
    if not id_match:
        errors.append("  - Missing 'id:' field")
    if not name_match:
        errors.append("  - Missing 'name:' field (must be quoted)")
    if not class_match:
        errors.append("  - Missing 'class:' field")
    
    if errors:
        print(f"ERROR: Invalid @progbox-register block in {filepath}:", file=sys.stderr)
        for err in errors:
            print(err, file=sys.stderr)
        sys.exit(1)
    
    return ProgressionEntry(
        id=id_match.group(1),
        name=name_match.group(1),
        class_name=class_match.group(1),
        header=str(filepath)
    )


def scan_directory(scan_dir: Path) -> List[ProgressionEntry]:
    """Scan directory recursively for progression headers."""
    entries = []
    
    if not scan_dir.exists():
        print(f"ERROR: Scan directory does not exist: {scan_dir}", file=sys.stderr)
        sys.exit(1)
    
    for hpp_file in sorted(scan_dir.rglob("*.hpp")):
        # Skip the generator script itself if it's somehow a .hpp
        if hpp_file.name == "generate_progression_registry.hpp":
            continue
            
        try:
            content = hpp_file.read_text(encoding='utf-8')
        except Exception as e:
            print(f"WARNING: Could not read {hpp_file}: {e}", file=sys.stderr)
            continue
            
        entry = parse_register_block(content, hpp_file)
        if entry:
            entries.append(entry)
            print(f"  Found: {entry.id} ({entry.class_name}) in {hpp_file.name}")
    
    return entries

def generate_header(entries: List[ProgressionEntry], output_path: Path, source_dir: Path) -> None:
    """Generate the C++ header file."""
    
    lines = [
        "/// @file generated_progression_registry.hpp",
        "/// @brief Auto-generated progression registry entries.",
        "/// @warning DO NOT EDIT - generated by generate_progression_registry.py",
        "///",
        f"/// Source directory scanned: {source_dir}",
        f"/// Registered strategies: {len(entries)}",
        "",
        "#pragma once",
        "",
        "// ============================================================================",
        "// REQUIRED INCLUDES",
        "// ============================================================================",
        "",
        "#include \"i_progression.hpp\"",
        "",
        "// ============================================================================",
        "// PROGRESSION IMPLEMENTATION INCLUDES",
        "// ============================================================================",
    ]
    
    # Use ONLY the filename (no directory path) since scripts/ is already in include_directories
    for entry in entries:
        header_filename = Path(entry.header).name
        lines.append(f'#include "{header_filename}"')
    
    lines.extend([
        "",
        "// ============================================================================",
        "// GENERATED REGISTRY ENTRIES",
        "// ============================================================================",
        "",
        "namespace progbox {",
        "namespace generated {",
        "",
        "/// @brief Get auto-generated progression registry entries.",
        "inline const std::vector<ProgressionEntry>& entries() {",
        "    static const std::vector<ProgressionEntry> registry = {",
    ])
    
    for entry in entries:
        lines.extend([
            "        {",
            f'            "{entry.id}",',
            f'            "{entry.name}",',
            f"            []() -> std::unique_ptr<IProgressionStrategy> {{ return std::make_unique<{entry.class_name}>(); }}",
            "        },",
        ])
    
    lines.extend([
        "    };",
        "    return registry;",
        "}",
        "",
        "} // namespace generated",
        "} // namespace progbox",
    ])
    
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("\n".join(lines) + "\n", encoding='utf-8')
    print(f"Generated: {output_path} ({len(entries)} entries)")


def main():
    parser = argparse.ArgumentParser(
        description="Generate progression registry header from annotated source files"
    )
    parser.add_argument("--scan-dir", required=True, help="Directory to scan")
    parser.add_argument("--output", required=True, help="Output header file path")
    parser.add_argument("--source-dir", default=None, help="Base source directory")
    
    args = parser.parse_args()
    
    scan_path = Path(args.scan_dir).resolve()
    output_path = Path(args.output).resolve()
    source_dir = Path(args.source_dir).resolve() if args.source_dir else scan_path
    
    print(f"Scanning: {scan_path}")
    
    entries = scan_directory(scan_path)
    
    if not entries:
        print("WARNING: No @progbox-register blocks found", file=sys.stderr)
        # Don't exit with error - allow empty registry
    
    generate_header(entries, output_path, source_dir)


if __name__ == "__main__":
    main()